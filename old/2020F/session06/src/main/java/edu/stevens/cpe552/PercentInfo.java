public class PercentInfo {
    float px, dx, py, dy, pw, dw, ph, dh;
    public PercentInfo(float px, float dx, float py, float dy, float pw, float dw, float ph, float dh) {
        this.px = px; this.dx = dx;
        this.py = py; this.dy = dy;
        this.pw = pw; this.dw = dw;
        this.ph = ph; this.dh = dh;
    }
            
}
